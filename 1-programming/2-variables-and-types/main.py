"""
main.py

Python examples for variables and data types.
"""

def variable_examples():
    age = 25               # int
    height = 5.9           # float
    is_student = True      # bool
    name = "Alice"         # string

    print(f"Age: {age}")
    print(f"Height: {height}")
    print(f"Is Student: {is_student}")
    print(f"Name: {name}")

if __name__ == "__main__":
    variable_examples()
